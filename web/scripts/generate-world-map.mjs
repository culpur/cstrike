#!/usr/bin/env node
/**
 * Generate pre-computed SVG path data from Natural Earth TopoJSON.
 * Output: src/data/world-map-paths.ts
 *
 * Uses the same equirectangular projection as BattleMapView/GeoMapView:
 *   x = ((lng + 180) / 360) * 700 + 50
 *   y = ((90 - lat) / 180) * 350 + 25
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import * as topojson from 'topojson-client';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

// Load Natural Earth 110m countries TopoJSON
const topoPath = join(ROOT, 'node_modules/world-atlas/countries-110m.json');
const topo = JSON.parse(readFileSync(topoPath, 'utf8'));

// Convert to GeoJSON features
const geo = topojson.feature(topo, topo.objects.countries);

// Same projection used in the map components
function project(lat, lng) {
  const x = ((lng + 180) / 360) * 700 + 50;
  const y = ((90 - lat) / 180) * 350 + 25;
  return [x, y];
}

// Convert a ring of [lng, lat] coordinates to SVG path commands
function ringToPath(ring) {
  return ring.map(([lng, lat], i) => {
    const [x, y] = project(lat, lng);
    return `${i === 0 ? 'M' : 'L'}${x.toFixed(1)},${y.toFixed(1)}`;
  }).join('') + 'Z';
}

// Convert a GeoJSON geometry to a single SVG path string
function geometryToPath(geometry) {
  if (geometry.type === 'Polygon') {
    return geometry.coordinates.map(ringToPath).join('');
  }
  if (geometry.type === 'MultiPolygon') {
    return geometry.coordinates.map(poly =>
      poly.map(ringToPath).join('')
    ).join('');
  }
  return '';
}

// Country name lookup from Natural Earth properties
// world-atlas uses numeric IDs matching ISO 3166-1 numeric codes
const countries = geo.features
  .map(f => ({
    id: f.id || f.properties?.iso_a3 || 'UNK',
    d: geometryToPath(f.geometry),
  }))
  .filter(c => c.d.length > 0);

// Also generate land outline (merged) for a single fill
const land = topojson.feature(topo, topo.objects.land);
const landPaths = land.features
  ? land.features.map(f => geometryToPath(f.geometry)).join('')
  : geometryToPath(land.geometry);

// Generate TypeScript output
const output = `/**
 * Pre-computed SVG path data from Natural Earth 110m countries.
 * Generated by scripts/generate-world-map.mjs — do not edit manually.
 *
 * Projection: equirectangular
 *   x = ((lng + 180) / 360) * 700 + 50
 *   y = ((90 - lat) / 180) * 350 + 25
 * ViewBox: 0 0 800 400
 */

/** Merged land outline — single path for all landmasses */
export const LAND_PATH = '${landPaths}';

/** Individual country polygons (ISO 3166-1 numeric ID) */
export const COUNTRY_PATHS: Array<{ id: string; d: string }> = ${JSON.stringify(countries, null, 0)};
`;

// Write output
mkdirSync(join(ROOT, 'src/data'), { recursive: true });
writeFileSync(join(ROOT, 'src/data/world-map-paths.ts'), output, 'utf8');

console.log(`Generated ${countries.length} country paths + land outline`);
console.log(`Output: src/data/world-map-paths.ts (${(output.length / 1024).toFixed(1)} KB)`);
